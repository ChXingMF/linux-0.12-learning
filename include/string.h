/*
 * This string-include defines all string functions as inline
 * functions. Use gcc. It also assumes ds=es=data space, this should be
 * normal. Most of the string-functions are rather heavily hand-optimized,
 * see especially strtok,strstr,str[c]spn. They should work, but are not
 * very easy to understand. Everything is done entirely within the register
 * set, making the functions fast and clean. String instructions have been
 * used through-out, making for "slightly" unclear code :-)
 *
 *		(C) 1991 Linus Torvalds
 */

 /*
 *	关键字inline和extern组合一起的作用几乎类同宏定义
 *  关键字inline和extern组合定义的函数放在.h头文件中，并且把包含关键字的另一个相同的函数定义放在一个库文件中。
 *	此时头文件中的定义会让绝大数对该函数的调用被替换嵌入
 */

/*
*   功能：将源字符串src复制到另一字符串dest，直到遇到null字符停止
*   参数：dest-目的字符串指针，src-源字符串指针
*            %0 - esi(src), %1 - edi(dest)
*/
extern inline char * strcpy(char * dest,const char *src)
{
__asm__("cld\n"								// 清方向位
	"1:\tlodsb\n\t"							// 加载DS:[esi]处1字节->al,并更新esi
	"stosb\n\t"								// 存储字节al->ES[edi],并更新edi
	"testb %%al,%%al\n\t"					// 刚刚储存的字节是否为0？
	"jne 1b"								// 不是，则向后跳转到标号1处，否则结束
	::"S" (src),"D" (dest):"si","di","ax");
return dest;								// // 返回目的字符串
}

extern inline char * strncpy(char * dest,const char *src,int count)
{
__asm__("cld\n"
	"1:\tdecl %2\n\t"
	"js 2f\n\t"
	"lodsb\n\t"
	"stosb\n\t"
	"testb %%al,%%al\n\t"
	"jne 1b\n\t"
	"rep\n\t"
	"stosb\n"
	"2:"
	::"S" (src),"D" (dest),"c" (count):"si","di","ax","cx");
return dest;
}

extern inline char * strcat(char * dest,const char * src)
{
__asm__("cld\n\t"
	"repne\n\t"
	"scasb\n\t"
	"decl %1\n"
	"1:\tlodsb\n\t"
	"stosb\n\t"
	"testb %%al,%%al\n\t"
	"jne 1b"
	::"S" (src),"D" (dest),"a" (0),"c" (0xffffffff):"si","di","ax","cx");
return dest;
}

extern inline char * strncat(char * dest,const char * src,int count)
{
__asm__("cld\n\t"
	"repne\n\t"
	"scasb\n\t"
	"decl %1\n\t"
	"movl %4,%3\n"
	"1:\tdecl %3\n\t"
	"js 2f\n\t"
	"lodsb\n\t"
	"stosb\n\t"
	"testb %%al,%%al\n\t"
	"jne 1b\n"
	"2:\txorl %2,%2\n\t"
	"stosb"
	::"S" (src),"D" (dest),"a" (0),"c" (0xffffffff),"g" (count)
	:"si","di","ax","cx");
return dest;
}

extern inline int strcmp(const char * cs,const char * ct)
{
register int __res __asm__("ax");
__asm__("cld\n"
	"1:\tlodsb\n\t"
	"scasb\n\t"
	"jne 2f\n\t"
	"testb %%al,%%al\n\t"
	"jne 1b\n\t"
	"xorl %%eax,%%eax\n\t"
	"jmp 3f\n"
	"2:\tmovl $1,%%eax\n\t"
	"jl 3f\n\t"
	"negl %%eax\n"
	"3:"
	:"=a" (__res):"D" (cs),"S" (ct):"si","di");
return __res;
}

/*
*   功能：字符串cs与字符串ct前count个字符进行比较
*   参数：count-比较的字符数
*   返回：如果cs>ct,则返回1；cs==ct，则返回0；cs<ct,返回-1
*   %0 寄存器eax（返回值__res）， %1 寄存器edi(字符串cs)， %2 寄存器esi(字符串ct)， %3 寄存器ecx(参数count)
*/
extern inline int strncmp(const char * cs,const char * ct,int count)
{
register int __res __asm__("ax");	// 寄存器变量__res
__asm__("cld\n"						// 清理方向位
	"1:\tdecl %3\n\t"				// count--
	"js 2f\n\t"						// 如果count < 0,则跳转到标号2
	"lodsb\n\t"						// 取串2的字符ds:[esi]->al,并且esi++
	"scasb\n\t"						// 比较al与字符串es:[edi],并且edi++
	"jne 3f\n\t"					// 如果不想等，则跳转到标号3
	"testb %%al,%%al\n\t"			// 判断该字符是否为NULL？
	"jne 1b\n"						// 不是NULL，则向后跳转到标号1，继续比较
	"2:\txorl %%eax,%%eax\n\t"		// 是NULL，则eax清零（返回值）
	"jmp 4f\n"						// 向前跳转到标号4，结束
	"3:\tmovl $1,%%eax\n\t"			// eax中置1
	"jl 4f\n\t"						// 如果前面比较中字符串2<字符串1，则返回1，结束
	"negl %%eax\n"					// 否则eax = -eax，返回负值，结束
	"4:"
	:"=a" (__res):"D" (cs),"S" (ct),"c" (count)		// 输出寄存器列表 a->寄存器eax D->edi S->esi c->ecx
	:"si","di","cx");
return __res;						// 返回比较结果
}

extern inline char * strchr(const char * s,char c)
{
register char * __res __asm__("ax");
__asm__("cld\n\t"
	"movb %%al,%%ah\n"
	"1:\tlodsb\n\t"
	"cmpb %%ah,%%al\n\t"
	"je 2f\n\t"
	"testb %%al,%%al\n\t"
	"jne 1b\n\t"
	"movl $1,%1\n"
	"2:\tmovl %1,%0\n\t"
	"decl %0"
	:"=a" (__res):"S" (s),"0" (c):"si");
return __res;
}

extern inline char * strrchr(const char * s,char c)
{
register char * __res __asm__("dx");
__asm__("cld\n\t"
	"movb %%al,%%ah\n"
	"1:\tlodsb\n\t"
	"cmpb %%ah,%%al\n\t"
	"jne 2f\n\t"
	"movl %%esi,%0\n\t"
	"decl %0\n"
	"2:\ttestb %%al,%%al\n\t"
	"jne 1b"
	:"=d" (__res):"0" (0),"S" (s),"a" (c):"ax","si");
return __res;
}

extern inline int strspn(const char * cs, const char * ct)
{
register char * __res __asm__("si");
__asm__("cld\n\t"
	"movl %4,%%edi\n\t"
	"repne\n\t"
	"scasb\n\t"
	"notl %%ecx\n\t"
	"decl %%ecx\n\t"
	"movl %%ecx,%%edx\n"
	"1:\tlodsb\n\t"
	"testb %%al,%%al\n\t"
	"je 2f\n\t"
	"movl %4,%%edi\n\t"
	"movl %%edx,%%ecx\n\t"
	"repne\n\t"
	"scasb\n\t"
	"je 1b\n"
	"2:\tdecl %0"
	:"=S" (__res):"a" (0),"c" (0xffffffff),"0" (cs),"g" (ct)
	:"ax","cx","dx","di");
return __res-cs;
}

